package com.contrato.entidad;


public class Solicitante {
	//atributos
	private int codSoli,celuSoli,dniSoli;
	private String nomSoli,apeSoli,distrito,direccSoli,sexo,tipoContrato;


	public int getCodSoli() {
		return codSoli;
	}
	public void setCodSoli(int codSoli) {
		this.codSoli = codSoli;
	}
	public int getCeluSoli() {
		return celuSoli;
	}
	public void setCeluSoli(int celuSoli) {
		this.celuSoli = celuSoli;
	}
	public int getDniSoli() {
		return dniSoli;
	}
	public void setDniSoli(int dniSoli) {
		this.dniSoli = dniSoli;
	}
	public String getNomSoli() {
		return nomSoli;
	}
	public void setNomSoli(String nomSoli) {
		this.nomSoli = nomSoli;
	}
	public String getApeSoli() {
		return apeSoli;
	}
	public void setApeSoli(String apeSoli) {
		this.apeSoli = apeSoli;
	}
	public String getDistrito() {
		return distrito;
	}
	public void setDistrito(String distrito) {
		this.distrito = distrito;
	}
	public String getDireccSoli() {
		return direccSoli;
	}
	public void setDireccSoli(String direccSoli) {
		this.direccSoli = direccSoli;
	}
	public String getSexo() {
		return sexo;
	}
	public void setSexo(String sexo) {
		this.sexo = sexo;
	}
	public String getTipoContrato() {
		return tipoContrato;
	}
	public void setTipoContrato(String tipoContrato) {
		this.tipoContrato = tipoContrato;
	}
	
	

}
